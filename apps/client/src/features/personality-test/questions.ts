import type { AxisScores, PersonalityCode, QuizOption, QuizQuestion } from './types';

type OptionTuple = [string, PersonalityCode, PersonalityCode, [number, number, number, number]];

function option(id: string, [text, primary, secondary, axes]: OptionTuple): QuizOption {
  return {
    id,
    text,
    personaScores: { [primary]: 3, [secondary]: 1 },
    axes: { appetite: axes[0], explore: axes[1], control: axes[2], social: axes[3] } as AxisScores,
  };
}

function question(id: string, title: string, options: OptionTuple[]): QuizQuestion {
  return {
    id,
    mode: Number(id.slice(1)) <= 8 ? 'quick' : 'full',
    title,
    options: options.map((item, index) => option(String.fromCharCode(65 + index), item)),
  };
}

